#include <string.h>
#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t			i;
	unsigned int	len;

	i = 0;
	len = ft_strlen(dest);
	while (src[i] != '\0')
	{
		dest[i + len] = src[i];
		i++;
	}
	dest[i + len] = '\0';
	return (dest);
}
